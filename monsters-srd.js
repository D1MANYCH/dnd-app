// CR в число для сортировки/фильтрации
function srdCrNum(cr) {
  if (cr === "0") return 0;
  if (cr === "1/8") return 0.125;
  if (cr === "1/4") return 0.25;
  if (cr === "1/2") return 0.5;
  var n = parseFloat(cr);
  return isNaN(n) ? 0 : n;
}

// Уникальные значения CR в порядке возрастания
function srdAllCRs() {
  var set = {};
  window.MONSTERS_SRD.forEach(function(m) { set[m.cr] = true; });
  return Object.keys(set).sort(function(a, b) { return srdCrNum(a) - srdCrNum(b); });
}

// Уникальные edition'ы
function srdAllEditions() {
  var set = {};
  window.MONSTERS_SRD.forEach(function(m) { if (m.edition) set[m.edition] = true; });
  return Object.keys(set).sort();
}

// Поиск по slug
function srdMonsterBySlug(slug) {
  for (var i = 0; i < window.MONSTERS_SRD.length; i++) {
    if (window.MONSTERS_SRD[i].slug === slug) return window.MONSTERS_SRD[i];
  }
  return null;
}

// Форматирует SRD-карточку в multi-line desc для PARTY_DATA.monsters
function srdMonsterToDesc(m) {
  if (!m) return "";
  var lines = [];
  var src = m.edition ? ("📜 SRD 5e · " + m.edition + " · CR " + m.cr + " (XP " + m.xp + ")")
                      : ("📜 SRD 5e · CR " + m.cr + " (XP " + m.xp + ")");
  lines.push(src);
  lines.push(m.size + " · КД " + m.ac + " · ХП " + m.hp + " (" + m.hpDice + ") · " + m.speed);
  var s = m.stats;
  lines.push("СИЛ " + s.str + " · ЛОВ " + s.dex + " · ТЕЛ " + s.con + " · ИНТ " + s.int + " · МУД " + s.wis + " · ХАР " + s.cha);
  if (m.saves)  lines.push("Спасброски: " + m.saves);
  if (m.skills) lines.push("Навыки: " + m.skills);
  if (m.senses) lines.push("Чувства: " + m.senses);
  if (m.langs)  lines.push("Языки: " + m.langs);
  if (m.traits && m.traits.length) {
    lines.push("");
    lines.push("— Особенности —");
    m.traits.forEach(function(t) { lines.push("• " + t.name + ". " + t.desc); });
  }
  if (m.actions && m.actions.length) {
    lines.push("");
    lines.push("— Действия —");
    m.actions.forEach(function(a) { lines.push("• " + a.name + ". " + a.desc); });
  }
  if (m.reactions && m.reactions.length) {
    lines.push("");
    lines.push("— Реакции —");
    m.reactions.forEach(function(r) { lines.push("• " + r.name + ". " + r.desc); });
  }
  return lines.join("\n");
}

window.srdCrNum = srdCrNum;
