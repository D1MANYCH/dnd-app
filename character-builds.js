// BUILD-NOTES-1: схема — каждое текстовое поле заметок может быть массивом вариантов.
// pickBuildVariant(arr, seed) — детерминированный выбор по seed (обычно char.id), либо случайный.
window.pickBuildVariant = function(arr, seed) {
  if (!Array.isArray(arr) || !arr.length) return "";
  if (arr.length === 1) return arr[0];
  var idx;
  if (seed != null && seed !== "") {
    var s = String(seed), h = 0;
    for (var i = 0; i < s.length; i++) h = ((h << 5) - h + s.charCodeAt(i)) | 0;
    idx = Math.abs(h) % arr.length;
  } else {
    idx = Math.floor(Math.random() * arr.length);
  }
  return arr[idx];
};

// Нормализация: строка → [строка], массив → массив, отсутствие → []. Поля appearance/personality/ideals/bonds/flaws/hooks/backstories.
window.normalizeBuildNotes = function(n) {
  if (!n || typeof n !== "object") return null;
  var out = {};
  ["appearance","personality","ideals","bonds","flaws"].forEach(function(k){
    var v = n[k];
    if (Array.isArray(v)) out[k] = v.filter(function(x){ return typeof x === "string" && x.trim(); });
    else if (typeof v === "string" && v.trim()) out[k] = [v];
    else out[k] = [];
  });
  out.hooks = Array.isArray(n.hooks) ? n.hooks.filter(function(x){ return typeof x === "string" && x.trim(); }) : [];
  out.backstories = Array.isArray(n.backstories) ? n.backstories.filter(function(x){ return typeof x === "string" && x.trim(); }) : [];
  return out;
};

// Прикрепляем к самим билдам, чтобы applyBuild() мог читать b.notes без отдельного lookup.
// PERF-2: build-notes-data.js грузится лениво (ensureBuildNotes в index.html) и зовёт
// attachBuildNotes повторно после подгрузки карты; вызов идемпотентен (!list[i].notes).
window.attachBuildNotes = function () {
  var list = window.CHARACTER_BUILDS || [];
  var map = window.BUILD_NOTES || {};
  for (var i = 0; i < list.length; i++) {
    var n = map[list[i].id];
    if (n && !list[i].notes) list[i].notes = window.normalizeBuildNotes(n);
  }
};
