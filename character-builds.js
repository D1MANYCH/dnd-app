window.getBuildsForClass = function(className) {
  if (!className) return [];
  return (window.CHARACTER_BUILDS || []).filter(function(b){ return b.className === className; });
};

window.getBuildById = function(buildId) {
  if (!buildId) return null;
  var list = window.CHARACTER_BUILDS || [];
  for (var i = 0; i < list.length; i++) {
    if (list[i].id === buildId) return list[i];
  }
  return null;
};

window.getBuildRecommendationForLevel = function(buildId, level) {
  var b = window.getBuildById(buildId);
  if (!b || !b.levelUp) return null;
  var rec = b.levelUp[level];
  return rec || null;
};
