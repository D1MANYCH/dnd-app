// ── Хелперы ────────────────────────────────────────────────
function npcArchBySlug(slug) {
  for (var i = 0; i < window.NPC_ARCHETYPES.length; i++) {
    if (window.NPC_ARCHETYPES[i].slug === slug) return window.NPC_ARCHETYPES[i];
  }
  return null;
}

function npcArchAttitudes() {
  return ["дружелюбный", "нейтральный", "враждебный", "неизв."];
}

window.npcArchBySlug = npcArchBySlug;
window.npcArchAttitudes = npcArchAttitudes;
